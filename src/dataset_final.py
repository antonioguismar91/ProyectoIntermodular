import pandas as pd
import numpy as np
import matplotlib.pyplot as plt
import seaborn as sns
import warnings
warnings.filterwarnings('ignore')

from sklearn.model_selection import train_test_split, StratifiedKFold, cross_val_score
from sklearn.preprocessing import RobustScaler
from sklearn.linear_model import LogisticRegression
from sklearn.ensemble import RandomForestClassifier, StackingClassifier
from sklearn.metrics import (roc_auc_score, roc_curve, f1_score,
                             classification_report, confusion_matrix,
                             recall_score, precision_score)
from xgboost import XGBClassifier
from lightgbm import LGBMClassifier
from catboost import CatBoostClassifier
import shap
from scipy.stats import norm, gamma, truncnorm, bernoulli
from scipy.linalg import cholesky
import joblib

print("="*80)
print("PIPELINE COMPLETO DE MODELADO – ENSEMBLE STACKING + SHAP")
print("="*80)

# ================================================================
# 1. GENERACIÓN DEL DATASET SINTÉTICO (CÓPULA GAUSSIANA NO LINEAL)
# ================================================================
print("\n Generando dataset sintético...")

n_samples = 30000
np.random.seed(42)

var_names = ['distance_m','hsr_distance_m','acwr','ctl','atl',
             'fatigue_score','sleep_quality','stress','muscle_soreness']

corr_matrix = np.array([
    [1.00, 0.75, 0.60, 0.20, 0.35,-0.40,-0.25,-0.10,-0.15],
    [0.75, 1.00, 0.65, 0.15, 0.30,-0.45,-0.20,-0.10,-0.10],
    [0.60, 0.65, 1.00, 0.10, 0.45,-0.30,-0.20,-0.15,-0.10],
    [0.20, 0.15, 0.10, 1.00, 0.70, 0.10,-0.10, 0.05, 0.05],
    [0.35, 0.30, 0.45, 0.70, 1.00, 0.15,-0.05, 0.10, 0.10],
    [-0.40,-0.45,-0.30, 0.10, 0.15, 1.00, 0.50, 0.40, 0.35],
    [-0.25,-0.20,-0.20,-0.10,-0.05, 0.50, 1.00, 0.30, 0.25],
    [-0.10,-0.10,-0.15, 0.05, 0.10, 0.40, 0.30, 1.00, 0.45],
    [-0.15,-0.10,-0.10, 0.05, 0.10, 0.35, 0.25, 0.45, 1.00]
])

eigvals = np.linalg.eigvals(corr_matrix)
if np.min(eigvals) < 1e-6:
    corr_matrix += np.eye(len(corr_matrix)) * (1e-6 - np.min(eigvals))

L = cholesky(corr_matrix, lower=True)
Z = np.random.randn(n_samples, len(var_names))
U_lat = Z @ L.T
U = norm.cdf(U_lat)

data = pd.DataFrame()

a1, b1 = (1500 - 5000) / 1000, (8500 - 5000) / 1000
data['distance_m'] = truncnorm.ppf(U[:, 0], a1, b1, loc=5000, scale=1000).astype(int)

data['hsr_distance_m'] = gamma.ppf(U[:, 1], a=4, scale=80).astype(int)

a3, b3 = (0.8 - 1.3) / 0.25, (2.5 - 1.3) / 0.25
data['acwr'] = truncnorm.ppf(U[:, 2], a3, b3, loc=1.3, scale=0.25)

a4, b4 = (20 - 50) / 10, (80 - 50) / 10
data['ctl'] = truncnorm.ppf(U[:, 3], a4, b4, loc=50, scale=10)

a5, b5 = (30 - 70) / 15, (110 - 70) / 15
data['atl'] = truncnorm.ppf(U[:, 4], a5, b5, loc=70, scale=15)

def ordinal_ppf(u, a, b, loc, scale):
    cont = truncnorm.ppf(u, (a - loc)/scale, (b - loc)/scale, loc=loc, scale=scale)
    return np.clip(np.round(cont), 1, 10).astype(int)

data['fatigue_score']   = ordinal_ppf(U[:, 5], 1, 10, 5.5, 1.8)
data['sleep_quality']   = ordinal_ppf(U[:, 6], 1, 10, 7.0, 1.5)
data['stress']          = ordinal_ppf(U[:, 7], 1, 10, 4.0, 1.5)
data['muscle_soreness'] = ordinal_ppf(U[:, 8], 1, 10, 3.0, 1.2)

acwr      = data['acwr']
fatigue   = data['fatigue_score']
stress    = data['stress']
sleep     = data['sleep_quality']
soreness  = data['muscle_soreness']

# Interacciones como variables explícitas (para el modelo)
data['inter_acwr_fatigue'] = (acwr - 1.3) * (fatigue - 5)
data['th_acwr_fatigue']    = ((acwr > 1.5) & (fatigue > 7)).astype(int)
data['th_stress_sleep']    = ((stress > 7) & (sleep < 4)).astype(int)
data['inter_sleep_sore']   = (6 - sleep) * (soreness - 3)

logit = (
    -2.2
    + 2.5 * (acwr - 1.3)
    + 0.6 * (fatigue / 10)
    + 0.4 * (stress / 10)
    - 0.3 * (sleep / 10)
    + 0.5 * (soreness / 10)
    + 1.2 * data['inter_acwr_fatigue']
    + 2.0 * data['th_acwr_fatigue']
    + 1.8 * data['th_stress_sleep']
    + 0.8 * data['inter_sleep_sore']
)

prob_injury = 1 / (1 + np.exp(-logit))
data['injury_risk'] = bernoulli.rvs(prob_injury)

data['player_id'] = [f"P{str(i).zfill(4)}" for i in range(n_samples)]
data['date'] = pd.date_range('2023-01-01', periods=n_samples, freq='D')

columnas_finales = ['player_id', 'date'] + var_names + [
    'inter_acwr_fatigue','th_acwr_fatigue','th_stress_sleep','inter_sleep_sore','injury_risk'
]
data = data[columnas_finales]

print("=== MUESTRA DEL DATASET ===")
print(data.head())
print(f"\nProporción de lesionados: {data['injury_risk'].mean():.1%}")

plt.figure(figsize=(6,4))
plt.boxplot([data.loc[data['injury_risk']==0, 'acwr'],
             data.loc[data['injury_risk']==1, 'acwr']],
            labels=['No lesión', 'Lesión'])
plt.title('ACWR por estado de lesión')
plt.ylabel('ACWR')
plt.grid(alpha=0.3)
plt.tight_layout()
plt.show()

data.to_csv('dataset_sintetico_soccermon.csv', index=False, encoding='utf-8-sig')
print("Dataset guardado: dataset_sintetico_soccermon.csv")

# ================================================================
# 2. ANÁLISIS EXPLORATORIO DE DATOS (EDA)
# ================================================================
print("\n EDA...")

df = pd.read_csv('dataset_sintetico_soccermon.csv')
print(f"Tamaño: {df.shape}")
print(f"Lesionados: {df['injury_risk'].sum()} ({df['injury_risk'].mean()*100:.1f}%)")

features = ['distance_m','hsr_distance_m','acwr','ctl','atl',
            'fatigue_score','sleep_quality','stress','muscle_soreness']

fig, axes = plt.subplots(3, 3, figsize=(15, 12))
for idx, col in enumerate(features):
    ax = axes.flat[idx]
    ax.hist(df[col], bins=30, alpha=0.7, color='steelblue', edgecolor='white')
    ax.set_title(col, fontsize=11)
plt.tight_layout()
plt.savefig('distribuciones_marginales.png', dpi=150)
plt.show()

fig, axes = plt.subplots(3, 3, figsize=(15, 12))
for idx, col in enumerate(features):
    ax = axes.flat[idx]
    df.boxplot(column=col, by='injury_risk', ax=ax, grid=False)
    ax.set_title(col, fontsize=11)
    ax.set_xlabel('')
    ax.set_ylabel('')
plt.suptitle('')
plt.tight_layout()
plt.savefig('boxplots_por_lesion.png', dpi=150)
plt.show()

plt.figure(figsize=(10, 8))
corr = df[features + ['injury_risk']].corr()
sns.heatmap(corr, annot=True, fmt='.2f', cmap='coolwarm', center=0,
            linewidths=0.5, annot_kws={'size': 8})
plt.title('Matriz de correlaciones')
plt.tight_layout()
plt.savefig('correlaciones.png', dpi=150)
plt.show()

top5 = ['acwr','fatigue_score','stress','sleep_quality','muscle_soreness']
sns.pairplot(df[top5 + ['injury_risk']], hue='injury_risk', diag_kind='kde', plot_kws={'alpha':0.5})
plt.savefig('pairplot_top5.png', dpi=150)
plt.show()
print("EDA TERMINADO")

# ================================================================
# 3. PREPROCESAMIENTO Y SPLIT ESTRATIFICADO
# ================================================================
print("\n Preprocesamiento y split...")

feature_cols = features + ['inter_acwr_fatigue','th_acwr_fatigue','th_stress_sleep','inter_sleep_sore']
X = df[feature_cols].copy()
y = df['injury_risk'].copy()

scaler = RobustScaler()
X_scaled = scaler.fit_transform(X)

X_train, X_test, y_train, y_test = train_test_split(
    X_scaled, y, test_size=0.2, random_state=42, stratify=y
)
print(f"Train: {X_train.shape[0]} | Test: {X_test.shape[0]}")
print(f"Lesionados train: {y_train.sum()} ({y_train.mean()*100:.1f}%)")
print(f"Lesionados test:  {y_test.sum()} ({y_test.mean()*100:.1f}%)")

scale_pos_weight = (y_train == 0).sum() / (y_train == 1).sum()
print(f"scale_pos_weight: {scale_pos_weight:.1f}")

# ================================================================
# 4. ENTRENAMIENTO DE MODELOS BASE (MEJORADOS)
# ================================================================
print("\n Entrenando modelos base...")

print("\n--- Baseline (LogisticRegression) ---")
lr = LogisticRegression(class_weight='balanced', max_iter=5000, random_state=42)
lr.fit(X_train, y_train)
y_pred_lr = lr.predict_proba(X_test)[:, 1]
auc_lr = roc_auc_score(y_test, y_pred_lr)
print(f"Baseline AUC: {auc_lr:.4f}")

print("\n--- XGBoost (mejorado) ---")
xgb_clf = XGBClassifier(
    n_estimators=300, max_depth=6, learning_rate=0.05,
    scale_pos_weight=scale_pos_weight, subsample=0.8, colsample_bytree=0.8,
    reg_alpha=0.1, reg_lambda=0.1, random_state=42, verbosity=0
)
xgb_clf.fit(X_train, y_train)
y_pred_xgb = xgb_clf.predict_proba(X_test)[:, 1]
auc_xgb = roc_auc_score(y_test, y_pred_xgb)
print(f"XGBoost AUC: {auc_xgb:.4f}")

print("\n--- LightGBM (mejorado) ---")
lgb_clf = LGBMClassifier(
    n_estimators=300, max_depth=6, learning_rate=0.05,
    scale_pos_weight=scale_pos_weight, subsample=0.8, colsample_bytree=0.8,
    reg_alpha=0.1, reg_lambda=0.1, random_state=42, verbose=-1
)
lgb_clf.fit(X_train, y_train)
y_pred_lgb = lgb_clf.predict_proba(X_test)[:, 1]
auc_lgb = roc_auc_score(y_test, y_pred_lgb)
print(f"LightGBM AUC: {auc_lgb:.4f}")

print("\n--- CatBoost (mejorado) ---")
cb_clf = CatBoostClassifier(
    iterations=300, depth=6, learning_rate=0.05,
    scale_pos_weight=float(scale_pos_weight), subsample=0.8,
    l2_leaf_reg=3, random_state=42, verbose=0
)
cb_clf.fit(X_train, y_train)
y_pred_cb = cb_clf.predict_proba(X_test)[:, 1]
auc_cb = roc_auc_score(y_test, y_pred_cb)
print(f"CatBoost AUC: {auc_cb:.4f}")

print("\n--- RandomForest (mejorado) ---")
rf_clf = RandomForestClassifier(
    n_estimators=300, max_depth=12, class_weight='balanced',
    random_state=42, n_jobs=-1
)
rf_clf.fit(X_train, y_train)
y_pred_rf = rf_clf.predict_proba(X_test)[:, 1]
auc_rf = roc_auc_score(y_test, y_pred_rf)
print(f"RandomForest AUC: {auc_rf:.4f}")

# ================================================================
# 5. STACKING ENSEMBLE (META-MODELO REGRESIÓN LOGÍSTICA)
# ================================================================
print("\n  Entrenando Stacking Ensemble...")

stack = StackingClassifier(
    estimators=[
        ('xgb', xgb_clf),
        ('lgb', lgb_clf),
        ('cat', cb_clf),
        ('rf', rf_clf)
    ],
    final_estimator=LogisticRegression(class_weight='balanced', max_iter=5000, random_state=42),
    cv=5,
    stack_method='predict_proba',
    n_jobs=-1
)
stack.fit(X_train, y_train)
y_pred_stack = stack.predict_proba(X_test)[:, 1]
auc_stack = roc_auc_score(y_test, y_pred_stack)
print(f"Stacking Ensemble AUC: {auc_stack:.4f}")

# ================================================================
# 6. OPTIMIZACIÓN DEL UMBRAL DE DECISIÓN (MAX F1)
# ================================================================
print("\n  Optimizando umbral de decisión...")

fpr, tpr, thresholds = roc_curve(y_test, y_pred_stack)
with np.errstate(divide='ignore', invalid='ignore'):
    f1_scores = 2 * (tpr * (1 - fpr)) / (tpr + (1 - fpr) + 1e-10)
optimal_idx = np.argmax(f1_scores)
optimal_threshold = thresholds[optimal_idx]
print(f"Umbral óptimo (max F1): {optimal_threshold:.4f}")

y_pred_stack_opt = (y_pred_stack >= optimal_threshold).astype(int)

# ================================================================
# 7. EVALUACIÓN FINAL Y GUARDADO DE RESULTADOS
# ================================================================
print("\n Evaluación final...")

modelos = {
    'Baseline (LR)':    (y_pred_lr, False),
    'XGBoost':          (y_pred_xgb, False),
    'LightGBM':         (y_pred_lgb, False),
    'CatBoost':         (y_pred_cb, False),
    'RandomForest':     (y_pred_rf, False),
    'Stacking (0.5)':   (y_pred_stack, False),
    'Stacking (opt)':   (y_pred_stack_opt, True)
}

print("\n" + "="*85)
print(f"{'Modelo':<20} {'AUC':<10} {'F1':<10} {'Recall':<10} {'Precision':<10}")
print("="*85)
for nombre, (pred, es_clase) in modelos.items():
    if es_clase:
        auc_val = roc_auc_score(y_test, pred)
        f1_val = f1_score(y_test, pred)
        rec_val = recall_score(y_test, pred)
        prec_val = precision_score(y_test, pred)
    else:
        auc_val = roc_auc_score(y_test, pred)
        pred_class = (pred >= 0.5).astype(int)
        f1_val = f1_score(y_test, pred_class)
        rec_val = recall_score(y_test, pred_class)
        prec_val = precision_score(y_test, pred_class)
    print(f"{nombre:<20} {auc_val:<10.4f} {f1_val:<10.4f} {rec_val:<10.4f} {prec_val:<10.4f}")

# Matriz de confusión
plt.figure(figsize=(6,5))
cm_opt = confusion_matrix(y_test, y_pred_stack_opt)
sns.heatmap(cm_opt, annot=True, fmt='d', cmap='Blues',
            xticklabels=['No Lesion', 'Lesion'], yticklabels=['No Lesion', 'Lesion'])
plt.title(f'Matriz de Confusion - Stacking (umbral={optimal_threshold:.3f})')
plt.xlabel('Prediccion')
plt.ylabel('Real')
plt.tight_layout()
plt.savefig('confusion_matrix_opt.png', dpi=150)
plt.show()

# Curvas ROC
plt.figure(figsize=(8, 6))
for nombre, (pred, es_clase) in modelos.items():
    if not es_clase:
        fpr_model, tpr_model, _ = roc_curve(y_test, pred)
        plt.plot(fpr_model, tpr_model, label=f'{nombre} (AUC={roc_auc_score(y_test, pred):.3f})')
plt.plot(fpr[optimal_idx], tpr[optimal_idx], 'o', color='red', markersize=8,
         label=f'Umbral óptimo ({optimal_threshold:.3f})')
plt.plot([0, 1], [0, 1], 'k--', alpha=0.5)
plt.xlabel('False Positive Rate')
plt.ylabel('True Positive Rate')
plt.title('Curvas ROC - Comparativa de Modelos')
plt.legend(loc='lower right')
plt.grid(alpha=0.3)
plt.tight_layout()
plt.savefig('curvas_roc.png', dpi=150)
plt.show()

# SHAP
print("\nGenerando SHAP para XGBoost...")
explainer = shap.TreeExplainer(xgb_clf)
shap_values = explainer.shap_values(X_test[:300])

shap.summary_plot(shap_values, X_test[:300], feature_names=feature_cols, show=False)
plt.title('SHAP Summary Plot - XGBoost')
plt.tight_layout()
plt.savefig('shap_summary.png', dpi=150)
plt.show()

shap.summary_plot(shap_values, X_test[:300], feature_names=feature_cols,
                  plot_type='bar', show=False)
plt.title('Importancia media SHAP')
plt.tight_layout()
plt.savefig('shap_importance.png', dpi=150)
plt.show()

# Guardar modelos
joblib.dump(scaler, 'scaler.pkl')
joblib.dump(lr,     'modelo_regresion_logistica.pkl')
joblib.dump(xgb_clf,'modelo_xgboost.pkl')
joblib.dump(lgb_clf,'modelo_lightgbm.pkl')
joblib.dump(cb_clf, 'modelo_catboost.pkl')
joblib.dump(rf_clf, 'modelo_random_forest.pkl')
joblib.dump(stack, 'modelo_stacking_ensemble.pkl')

